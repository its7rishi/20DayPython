# Python Data Types
""" 
Text = str
Numeric = int, float, complex
Sequence Types = list, tuple, range
Mapping Type = dict
Set Types = set, frozenset
Boolean Type = bool
Binary Types = bytes, bytearray, memoryview
None Type = NoneType
 """

# ? Getting Data Type
# type() command
""" 
x = 5 
print(type(x))
 """

# ? Setting Data Type
# Data Type is set when the variable is declared
""" 
# str
a = "Hello World"

# int
b = 20

# float
c = 30.22

# complex
d = 1j

# list
e = ["apple", "banana", "cherry"]

# tuple
f = ("apple", "banana", "cherry")

# range
g = range(6)

# dict
my_dict = {"name": "Alex", age: 5}

# set
h = {"apple", "banana", "cherry"}

# frozenset
i = frozenset(("apple", "banana", "cherry"))

# bool
j = True

# bytes
k = b"Hello"

# bytearray
l = bytearray(5)

# memoryview
m = memoryview(bytes(5))

# NoneType
n = None
 """

# Specify Data Type
# To specify data type, use the constructor
""" 
my_name = str("John")
num = int(4)
my_float = float(3.22)
 """

